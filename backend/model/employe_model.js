const mongoose = require('mongoose');

const employerSchema = new Schema({
    name: {
        type: String,
        required: true,
    },
    surname: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    }
})
module.exports = mongoose.model('Employer', employerSchema);
